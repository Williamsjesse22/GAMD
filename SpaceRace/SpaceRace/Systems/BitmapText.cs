    /// <summary>Draws <paramref name="text"/> at <paramref name="position"/> with the given pixel scale.</summary>
    public static void Draw(SpriteBatch batch, Texture2D pixel, string text, Vector2 position, Color color, int scale = 2)
    {
        const int glyphW = 5, glyphH = 7, kern = 1;
        int x = (int)position.X;
        int y = (int)position.Y;
        foreach (char c in text)
        {
            if (Glyphs.TryGetValue(c, out var bits))
            {
                for (int row = 0; row < glyphH; row++)
                {
                    byte rowBits = bits[row];
                    for (int col = 0; col < glyphW; col++)
                    {
                        if ((rowBits & (1 << (glyphW - 1 - col))) != 0)
                        {
                            batch.Draw(pixel,
                                new Rectangle(x + col * scale, y + row * scale, scale, scale),
                                color);
                        }
                    }
                }
            }
            x += (glyphW + kern) * scale;
        }
    }

    /// <summary>Pack a 35-cell 0/1 grid into 7 bytes (one per row, MSB = leftmost column).</summary>
    private static byte[] Pack(this byte[] grid)
    {
        var rows = new byte[7];
        for (int r = 0; r < 7; r++)
        {
            byte b = 0;
            for (int c = 0; c < 5; c++)
            {
                if (grid[r * 5 + c] != 0) b |= (byte)(1 << (4 - c));
            }
            rows[r] = b;
        }
        return rows;
    }
